// Sử dụng hàm Math.abs()
console.log(Math.abs(-5.5)); // Kết quả: 5.5, vì Math.abs() trả về giá trị tuyệt đối của số truyền vào
console.log(Math.abs(10));  // Kết quả: 10, vì số dương không bị thay đổi giá trị tuyệt đối

// Sử dụng hàm Math.ceil()
console.log(Math.ceil(4.4)); // Kết quả: 5, vì Math.ceil() làm tròn lên số nguyên gần nhất
console.log(Math.ceil(5.8)); // Kết quả: 6, vì Math.ceil() làm tròn lên số nguyên gần nhất

// Sử dụng hàm Math.floor()
console.log(Math.floor(5.7)); // Kết quả: 5, vì Math.floor() làm tròn xuống số nguyên gần nhất
console.log(Math.floor(6.1)); // Kết quả: 6, vì Math.floor() làm tròn xuống số nguyên gần nhất

// Sử dụng hàm Math.round()
console.log(Math.round(10.1)); // Kết quả: 10, vì Math.round() làm tròn đến số nguyên gần nhất (10.1 gần 10 hơn)
console.log(Math.round(20.6)); // Kết quả: 21, vì Math.round() làm tròn đến số nguyên gần nhất (20.6 gần 21 hơn)
